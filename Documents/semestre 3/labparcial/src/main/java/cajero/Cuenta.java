package cajero;

/**
 *
 * @author bryan
 */
public class Cuenta { 
    private double saldo = 1000;

    public double getSaldo() {
        return saldo;
    }

    public boolean retirar(double monto) {
        if (monto <= saldo) {
            saldo -= monto;
            return true;
        }
        return false;
    }
}
    

